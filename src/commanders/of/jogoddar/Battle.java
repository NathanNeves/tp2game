    private void passarVez(){
        turno++;
        if(turno > 3 ){
            turno = 0;
            cont+=1;
        }
         contadordeturno.setText("Turno:"+this.cont);
         Vezde.setText("Vez de:"+jogadores.get(this.turno).getNome());
         jogadores.get(this.turno).checarBuff();
         jogadores.get(this.turno).checaDurabilidade();
         rodartabuleiro();
         checarcarta(turno);
         
    }
     
